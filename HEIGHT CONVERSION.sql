--NOW WE MOVE TO HEIGHT
--WE CALL HEIGHT USING '%''%"' TO SEE WHAT WE ARE TRYING TO CONVERT TO CM
SELECT HEIGHT FROM FIFA
WHERE HEIGHT LIKE '%''%"'

--HENCE WE RUN THE CONVERSION SYNTAX FEET AND INCHES TO CM
SELECT HEIGHT, CASE
				WHEN HEIGHT LIKE '%''%"' THEN 
				TRY_CONVERT(DECIMAL(10,2), SUBSTRING(HEIGHT,1, CHARINDEX('''', HEIGHT)-1))*30.48 + 
				TRY_CONVERT(DECIMAL(10,2), SUBSTRING(HEIGHT,CHARINDEX('''', HEIGHT)+1,
				LEN(HEIGHT)-CHARINDEX('''', HEIGHT)-1))*2.54 
				WHEN HEIGHT LIKE '%"' THEN 
				TRY_CONVERT(DECIMAL(10,2), SUBSTRING(HEIGHT,1, LEN(HEIGHT)-2))*2.54 
				ELSE  TRY_CONVERT(DECIMAL(10,2), SUBSTRING(HEIGHT,1, LEN(HEIGHT)-2))
				END
FROM FIFA

--WE UPDATE THE COLUMN HEIGHT
UPDATE FIFA
SET HEIGHT = CASE
				WHEN HEIGHT LIKE '%''%"' THEN 
				TRY_CONVERT(DECIMAL(10,2), SUBSTRING(HEIGHT,1, CHARINDEX('''', HEIGHT)-1))*30.48 + 
				TRY_CONVERT(DECIMAL(10,2), SUBSTRING(HEIGHT,CHARINDEX('''', HEIGHT)+1,
				LEN(HEIGHT)-CHARINDEX('''', HEIGHT)-1))*2.54 
				WHEN HEIGHT LIKE '%"' THEN 
				TRY_CONVERT(DECIMAL(10,2), SUBSTRING(HEIGHT,1, LEN(HEIGHT)-2))*2.54 
				ELSE  TRY_CONVERT(DECIMAL(10,2), SUBSTRING(HEIGHT,1, LEN(HEIGHT)-2))
				END
				
--LASTLY WE REPLACE '.0000' WITH ' CM'

SELECT REPLACE(HEIGHT,'.0000',' CM')
FROM FIFA

--THEN UPDATE COLUMN

UPDATE FIFA
SET HEIGHT = REPLACE(HEIGHT,'.0000',' CM')